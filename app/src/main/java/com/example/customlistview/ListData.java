package com.example.customlistview;

public class ListData {
    String name, time;
    int area, duration;
    int image;

    public ListData(String name, String time, int area, int duration, int image) {
        this.name = name;
        this.time = time;
        this.area = area;
        this.duration = duration;
        this.image = image;
    }
}